package otto

import "testing"

func TestDispatcher(t *testing.T) {
	disp := NewDispatcher()
	if disp.InQ == nil {
		t.Error("InQ is nil expected a channel")
	}

	if disp.StoreQ == nil {
		t.Error("StoreQ is nil expected a channel")
	}

	if disp.WebQ == nil {
		t.Error("WebQ is nil expected a channel")
	}

	if len(disp.WebQ) != 0 {
		t.Errorf("WebQ expected (0) got (%d)", len(disp.WebQ))
	}
}
